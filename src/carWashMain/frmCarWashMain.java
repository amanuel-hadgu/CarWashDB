    public static void setSqlDateOfToday(String strDateOfToday) {
        String subDateOfToday = strDateOfToday.substring(10, 14) + "-" + strDateOfToday.substring(4, 6) + "-" + strDateOfToday.substring(7, 9);
        try {
            frmCarWashMain.sqlDateOfToday = new java.sql.Date(new SimpleDateFormat("yyyy-MM-dd").parse(subDateOfToday).getTime());
        } catch (ParseException ex) {
            String str = "ናይ ሎሚ ዕለት ብትኽክል ምረጽ";
            String errorType = "ጌጋ";
            ConnectionManager.message(str, errorType);
        }
    }

